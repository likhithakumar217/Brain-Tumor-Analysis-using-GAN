# ==========================================
# TRAINING LOOP
# ==========================================

for epoch in range(EPOCHS):

    for mri, ct in loader:

        # ==================================
        # Move to GPU
        # ==================================

        mri = mri.to(device)

        ct = ct.to(device)

        # ==================================
        # Generate Fused Image
        # ==================================

        fake = generator(mri, ct)

        # ==================================
        # REAL & FAKE LABELS
        # ==================================

        real_labels = torch.ones(
            fake.size(0),
            1,
            30,
            30
        ).to(device)

        fake_labels = torch.zeros(
            fake.size(0),
            1,
            30,
            30
        ).to(device)

        # ==================================
        # TRAIN DISCRIMINATOR
        # ==================================

        d_optimizer.zero_grad()

        # Real loss

        real_output = discriminator(ct)

        d_real_loss = adversarial_loss(
            real_output,
            real_labels
        )

        # Fake loss

        fake_output = discriminator(
            fake.detach()
        )

        d_fake_loss = adversarial_loss(
            fake_output,
            fake_labels
        )

        # Total discriminator loss

        d_loss = (
            d_real_loss +
            d_fake_loss
        ) / 2

        d_loss.backward()

        d_optimizer.step()

        # ==================================
        # TRAIN GENERATOR
        # ==================================

        g_optimizer.zero_grad()

        fake_output = discriminator(fake)

        # GAN loss

        g_gan_loss = adversarial_loss(
            fake_output,
            real_labels
        )

        # L1 loss

        g_l1_loss = l1_loss(
            fake,
            (mri + ct) / 2
        )

        # SSIM loss

        g_ssim_loss = 1 - ssim(
            fake,
            (mri + ct) / 2,
            data_range=2,
            size_average=True
        )

        # Total generator loss

        g_loss = (
            g_gan_loss +
            100 * g_l1_loss +
            10 * g_ssim_loss
        )

        g_loss.backward()

        g_optimizer.step()

    # ======================================
    # PRINT LOSSES
    # ======================================

    print(
        f"Epoch [{epoch+1}/{EPOCHS}] "
        f"D Loss: {d_loss.item():.4f} "
        f"G Loss: {g_loss.item():.4f}"
    )

