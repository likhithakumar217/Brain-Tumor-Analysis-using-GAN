import torch
import torch.nn as nn

# ==========================================
# STEP 2 — Mish Activation
# ==========================================

class Mish(nn.Module):

    def forward(self, x):

        return x * torch.tanh(
            nn.functional.softplus(x)
        )

# ==========================================
# STEP 3 — Swish Activation
# ==========================================

class Swish(nn.Module):

    def forward(self, x):

        return x * torch.sigmoid(x)

# ==========================================
# STEP 4 — Encoder Block
# ==========================================

class EncoderBlock(nn.Module):

    def __init__(self,
                 in_channels,
                 out_channels,
                 activation='mish'):

        super().__init__()

        if activation == 'prelu':

            act = nn.PReLU()

        else:

            act = Mish()

        self.block = nn.Sequential(

            nn.Conv2d(
                in_channels,
                out_channels,
                kernel_size=3,
                stride=1,
                padding=1
            ),

            nn.InstanceNorm2d(out_channels),

            act,

            nn.Conv2d(
                out_channels,
                out_channels,
                kernel_size=3,
                stride=2,
                padding=1
            ),

            nn.InstanceNorm2d(out_channels),

            act
        )

    def forward(self, x):

        return self.block(x)

# ==========================================
# STEP 5 — Residual Block
# ==========================================

class ResidualBlock(nn.Module):

    def __init__(self, channels):

        super().__init__()

        self.block = nn.Sequential(

            nn.Conv2d(
                channels,
                channels,
                kernel_size=3,
                stride=1,
                padding=1
            ),

            nn.InstanceNorm2d(channels),

            Mish(),

            nn.Conv2d(
                channels,
                channels,
                kernel_size=3,
                stride=1,
                padding=1
            ),

            nn.InstanceNorm2d(channels),

            Swish()
        )

    def forward(self, x):

        return x + self.block(x)

# ==========================================
# STEP 6 — Decoder Block
# ==========================================

class DecoderBlock(nn.Module):

    def __init__(self,
                 in_channels,
                 out_channels):

        super().__init__()

        self.block = nn.Sequential(

            nn.Conv2d(
                in_channels,
                out_channels * 4,
                kernel_size=3,
                stride=1,
                padding=1
            ),

            nn.PixelShuffle(2),

            nn.InstanceNorm2d(out_channels),

            Mish()
        )

    def forward(self, x):

        return self.block(x)

# ==========================================
# STEP 7 — Generator
# ==========================================

class Generator(nn.Module):

    def __init__(self):

        super().__init__()

        # MRI Encoder

        self.mri_encoder1 = EncoderBlock(
            1, 64, activation='prelu'
        )

        self.mri_encoder2 = EncoderBlock(
            64, 128
        )

        self.mri_encoder3 = EncoderBlock(
            128, 256
        )

        # CT Encoder

        self.ct_encoder1 = EncoderBlock(
            1, 64, activation='prelu'
        )

        self.ct_encoder2 = EncoderBlock(
            64, 128
        )

        self.ct_encoder3 = EncoderBlock(
            128, 256
        )

        # Fusion Conv

        self.fusion = nn.Conv2d(
            512,
            512,
            kernel_size=3,
            stride=1,
            padding=1
        )

        # Residual Blocks

        self.res1 = ResidualBlock(512)

        self.res2 = ResidualBlock(512)

        self.res3 = ResidualBlock(512)

        # Decoder

        self.dec1 = DecoderBlock(
            512,
            256
        )

        self.dec2 = DecoderBlock(
            256,
            128
        )

        self.dec3 = DecoderBlock(
            128,
            64
        )

        # Final Output

        self.final = nn.Sequential(

            nn.Conv2d(
                64,
                1,
                kernel_size=3,
                stride=1,
                padding=1
            ),

            nn.Tanh()
        )

    def forward(self, mri, ct):

        # MRI Encoder

        m1 = self.mri_encoder1(mri)

        m2 = self.mri_encoder2(m1)

        m3 = self.mri_encoder3(m2)

        # CT Encoder

        c1 = self.ct_encoder1(ct)

        c2 = self.ct_encoder2(c1)

        c3 = self.ct_encoder3(c2)

        # Concatenate

        fused = torch.cat([m3, c3], dim=1)

        fused = self.fusion(fused)

        # Residual Learning

        fused = self.res1(fused)

        fused = self.res2(fused)

        fused = self.res3(fused)

        # Decoder

        d1 = self.dec1(fused)

        d2 = self.dec2(d1)

        d3 = self.dec3(d2)

        # Output

        out = self.final(d3)

        return out

